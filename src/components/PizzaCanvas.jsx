const centeredLayer = (size, zIndex) => ({
  position: 'absolute',
  top: '50%',
  left: '50%',
  transform: 'translate(-50%, -50%)',
  width: size,
  height: size,
  objectFit: 'contain',
  zIndex,
});

const previewBtn = {
  width: '100%',
  height: '100%',
  padding: 0,
  background: 'none',
  border: 'none',
  borderRadius: 0,
  cursor: 'pointer',
  overflow: 'visible',
};

export default function PizzaCanvas({
  activeCategory     = 'dough',
  selectedDough      = null,
  selectedSauce      = null,
  selectedCheese     = null,
  selectedMeats      = [],
  selectedVegetables = [],
  onDoughSelect      = () => {},
  onSauceSelect      = () => {},
  onCheeseSelect     = () => {},
  onMeatToggle       = () => {},
  onVegetableToggle  = () => {},
  size               = 'min(540px, 88vw, calc(100vh - 180px))',
}) {
  const activeDough   = selectedDough  ? DOUGHS.find(d => d.id === selectedDough)   : null;
  const activeSauce   = selectedSauce  ? SAUCES.find(s => s.id === selectedSauce)   : null;
  const activeCheese  = selectedCheese ? CHEESES.find(c => c.id === selectedCheese) : null;
  const activeMeats   = selectedMeats.map(id => MEATS.find(m => m.id === id)).filter(Boolean);
  const activeVeggies = selectedVegetables.map(id => VEGETABLES.find(v => v.id === id)).filter(Boolean);

  const atMeatLimit    = selectedMeats.length      >= 4;
  const atVeggieLimit  = selectedVegetables.length >= 6;

  return (
    <div className="pizza-canvas-wrap" style={{ position: 'relative', width: size, aspectRatio: '1' }}>

      {/* ── Layer 1: tray ── */}
      <img src={trayImg} alt="tray" style={centeredLayer('54%', 1)} />

      {/* ── Layer 2: dough ── */}
      {activeDough && (
        <img
          key={activeDough.id}
          src={activeDough.full}
          alt={activeDough.label}
          className="dough-layer"
          style={{ ...centeredLayer('49%', 2), pointerEvents: 'none' }}
        />
      )}

      {/* ── Layer 3: sauce ── */}
      {activeSauce && (
        <img
          key={activeSauce.id}
          src={activeSauce.layer}
          alt={activeSauce.label}
          className="sauce-layer"
          style={{ ...centeredLayer(activeSauce.layerSize ?? '42%', 3), pointerEvents: 'none' }}
        />
      )}

      {/* ── Layer 4: cheese ── */}
      {activeCheese && (
        <img
          key={activeCheese.id}
          src={activeCheese.layer}
          alt={activeCheese.label}
          className="cheese-layer"
          style={{
            ...centeredLayer('40%', 4),
            transform: `translate(-50%, calc(-50% + ${activeCheese.layerOffsetY ?? '0px'}))`,
            pointerEvents: 'none',
          }}
        />
      )}

      {/* ── Layer 5+: meat toppings — selection order = z-order ── */}
      {activeMeats.map((m, i) => (
        <img
          key={m.id}
          src={m.layer}
          alt={m.label}
          className="meat-layer"
          style={{ ...centeredLayer(m.layerSize ?? '42%', 5 + i), pointerEvents: 'none' }}
        />
      ))}

      {/* ── Layer above meats: vegetable toppings ── */}
      {activeVeggies.map((v, i) => (
        <img
          key={v.id}
          src={v.layer}
          alt={v.label}
          className="veggie-layer"
          style={{
            ...centeredLayer(v.layerSize ?? '42%', 5 + activeMeats.length + i),
            ...(v.layerOffsetY ? { transform: `translate(-50%, calc(-50% + ${v.layerOffsetY}))` } : {}),
            pointerEvents: 'none',
          }}
        />
      ))}

      {/* ── Dough previews (triangle) ── */}
      {activeCategory === 'dough' && DOUGHS.map(d => (
        <div
          key={d.id}
          className="preview-wrapper"
          style={{ position: 'absolute', width: '17%', height: '17%', zIndex: 10, ...d.pos }}
        >
          <button
            className="pizza-preview-btn"
            onClick={() => onDoughSelect(d.id)}
            aria-label={d.label}
            style={previewBtn}
          >
            <img src={d.preview} alt={d.label}
              style={{ width: '100%', height: '100%', objectFit: 'contain', display: 'block' }} />
          </button>
          <span className="dough-label">{d.label}</span>
        </div>
      ))}

      {/* ── Sauce previews (pentagon) ── */}
      {activeCategory === 'sauces' && SAUCES.map(s => (
        <div
          key={s.id}
          className="preview-wrapper"
          style={{ position: 'absolute', width: s.previewSize ?? '17%', height: s.previewSize ?? '17%', zIndex: 10, ...s.pos }}
        >
          <button
            className="pizza-preview-btn"
            onClick={() => onSauceSelect(s.id)}
            aria-label={s.label}
            style={previewBtn}
          >
            <img src={s.preview} alt={s.label}
              style={{ width: '100%', height: '100%', objectFit: 'contain', display: 'block' }} />
          </button>
          <span className="dough-label">{s.label}</span>
        </div>
      ))}

      {/* ── Cheese previews (triangle) ── */}
      {activeCategory === 'cheese' && CHEESES.map(c => (
        <div
          key={c.id}
          className="preview-wrapper"
          style={{ position: 'absolute', width: '17%', height: '17%', zIndex: 10, ...c.pos }}
        >
          <button
            className="pizza-preview-btn"
            onClick={() => onCheeseSelect(c.id)}
            aria-label={c.label}
            style={previewBtn}
          >
            <img src={c.preview} alt={c.label}
              style={{ width: '100%', height: '100%', objectFit: 'contain', display: 'block' }} />
          </button>
          <span className="dough-label">{c.label}</span>
        </div>
      ))}

      {/* ── Meat previews (9-point circle) ── */}
      {activeCategory === 'meat' && MEATS.map((m, i) => {
        const isSelected = selectedMeats.includes(m.id);
        const isDisabled = atMeatLimit && !isSelected;
        return (
          <div
            key={m.id}
            className={[
              'preview-wrapper',
              isSelected ? 'preview-wrapper--selected' : '',
              isDisabled ? 'preview-wrapper--disabled' : '',
            ].filter(Boolean).join(' ')}
            style={{
              position: 'absolute', width: '12.5%', height: '12.5%', zIndex: 10,
              animationDelay: `${i * 28}ms`,
              ...m.pos,
            }}
          >
            <button
              className="pizza-preview-btn"
              onClick={() => onMeatToggle(m.id)}
              aria-label={m.label}
              aria-pressed={isSelected}
              disabled={isDisabled}
              style={previewBtn}
            >
              <img src={m.preview} alt={m.label}
                style={{ width: '100%', height: '100%', objectFit: 'contain', display: 'block' }} />
            </button>
            <span className="dough-label">{m.label}</span>
          </div>
        );
      })}

      {/* ── Vegetable previews (14-point circle) ── */}
      {activeCategory === 'vegetables' && VEGETABLES.map((v, i) => {
        const isSelected = selectedVegetables.includes(v.id);
        const isDisabled = atVeggieLimit && !isSelected;
        return (
          <div
            key={v.id}
            className={[
              'preview-wrapper',
              isSelected ? 'preview-wrapper--selected' : '',
              isDisabled ? 'preview-wrapper--disabled' : '',
            ].filter(Boolean).join(' ')}
            style={{
              position: 'absolute', width: '11%', height: '11%', zIndex: 10,
              animationDelay: `${i * 28}ms`,
              ...v.pos,
            }}
          >
            <button
              className="pizza-preview-btn"
              onClick={() => onVegetableToggle(v.id)}
              aria-label={v.label}
              aria-pressed={isSelected}
              disabled={isDisabled}
              style={previewBtn}
            >
              <img src={v.preview} alt={v.label}
                style={{ width: '100%', height: '100%', objectFit: 'contain', display: 'block' }} />
            </button>
            <span className="dough-label">{v.label}</span>
          </div>
        );
      })}

      {/* ── Per-category topping limit message ── */}
      <div className={`topping-limit-msg${(atMeatLimit && activeCategory === 'meat') || (atVeggieLimit && activeCategory === 'vegetables') ? ' topping-limit-msg--visible' : ''}`}>
        {activeCategory === 'meat' ? 'Maximum 4 meat toppings' : 'Maximum 6 vegetable toppings'}
      </div>

    </div>
  );
}
